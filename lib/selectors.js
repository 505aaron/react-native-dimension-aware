export const getScreen = state => state.screen;
export const getScreenWidth = state => state.screen && state.screen.width;
export const getScreenHeight = state => state.screen && state.screen.height;

export const getWindow = state => state.window;
export const getWindowWidth = state => state.window && state.window.width;
export const getWindowHeight = state => state.window && state.window.height;
